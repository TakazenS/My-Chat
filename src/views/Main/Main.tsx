import { useState } from "react";
import { invoke } from "@tauri-apps/api/core";
import Settings from "../Settings/Settings";
import styles from "./Main.module.css";

interface MainProps {
  username: string;
  onLogout: () => void;
}

export default function Main({ username, onLogout }: MainProps) {
  const [showSettings, setShowSettings] = useState(false);

  async function handleLogout() {
    try {
      await invoke("logout");
      onLogout();
    } catch (err) {
      console.error("Erreur logout:", err);
    }
  }

  if (showSettings) {
    return <Settings username={username} onBack={() => setShowSettings(false)} />;
  }

  return (
    <main className={styles.main}>
      <section className={styles.leftPannel}>
        <div className={styles.leftPannelBottom}>
          <div 
            className={styles.settingsButton} 
            onClick={() => setShowSettings(true)} 
            title="Paramètres"
          >
            <svg xmlns="http://www.w3.org/2000/svg" width="22" height="22" viewBox="0 0 24 24" fill="none" stroke="white" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><circle cx="12" cy="12" r="3"></circle><path d="M19.4 15a1.65 1.65 0 0 0 .33 1.82l.06.06a2 2 0 0 1 0 2.83 2 2 0 0 1-2.83 0l-.06-.06a1.65 1.65 0 0 0-1.82-.33 1.65 1.65 0 0 0-1 1.51V21a2 2 0 0 1-2 2 2 2 0 0 1-2-2v-.09A1.65 1.65 0 0 0 9 19.4a1.65 1.65 0 0 0-1.82.33l-.06.06a2 2 0 0 1-2.83 0 2 2 0 0 1 0-2.83l.06-.06a1.65 1.65 0 0 0 .33-1.82 1.65 1.65 0 0 0-1.51-1H3a2 2 0 0 1-2-2 2 2 0 0 1 2-2h.09A1.65 1.65 0 0 0 4.6 9a1.65 1.65 0 0 0-.33-1.82l-.06-.06a2 2 0 0 1 0-2.83 2 2 0 0 1 2.83 0l.06.06a1.65 1.65 0 0 0 1.82.33H9a1.65 1.65 0 0 0 1-1.51V3a2 2 0 0 1 2-2 2 2 0 0 1 2 2v.09a1.65 1.65 0 0 0 1 1.51 1.65 1.65 0 0 0 1.82-.33l.06-.06a2 2 0 0 1 2.83 0 2 2 0 0 1 0 2.83l-.06.06a1.65 1.65 0 0 0-.33 1.82V9a1.65 1.65 0 0 0 1.51 1H21a2 2 0 0 1 2 2 2 2 0 0 1-2 2h-.09a1.65 1.65 0 0 0-1.51 1z"></path></svg>
          </div>
          <div className={styles.logoutButton} onClick={handleLogout} title="Déconnexion">
            <svg xmlns="http://www.w3.org/2000/svg" width="22" height="22" viewBox="0 0 24 24" fill="none" stroke="white" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><path d="M9 21H5a2 2 0 0 1-2-2V5a2 2 0 0 1 2-2h4"></path><polyline points="16 17 21 12 16 7"></polyline><line x1="21" y1="12" x2="9" y2="12"></line></svg>
          </div>
        </div>
      </section>
      <section className={styles.navigationPannel}>
        <div className={styles.pannelHeader}>Salons Vocaux</div>
      </section>
      <section className={styles.mainContent}>
        <div className={styles.chatHeader}>Bienvenue, {username}</div>
      </section>
    </main>
  );
}
